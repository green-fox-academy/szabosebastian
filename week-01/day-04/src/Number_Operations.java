public class Number_Operations {
    public static void main(String[] args) {
        System.out.println(1+1);
        System.out.println(5-2);
        System.out.println(3*4);
        System.out.println(6/2);
        System.out.println(5/2);
        System.out.println(5./2);
        System.out.println(5/2.);
        System.out.println(5./2.);
        System.out.println(5%2);
    }
}
